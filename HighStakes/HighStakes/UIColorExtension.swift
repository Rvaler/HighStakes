import UIKit

extension UIColor {
    
    static var App_Grey: UIColor {
        return UIColor(red: 238/255, green: 237/255, blue: 232/255, alpha: 1)
    }
    static var App_Orange: UIColor {
        return UIColor(red: 247/255, green: 151/255, blue: 82/255, alpha: 1)
    }
    static var App_Green: UIColor {
        return UIColor(red: 82/255, green: 188/255, blue: 136/255, alpha: 1)
    }
    static var App_Blue: UIColor {
        return UIColor(red: 66/255, green: 106/255, blue: 179/255, alpha: 1)
    }

}
